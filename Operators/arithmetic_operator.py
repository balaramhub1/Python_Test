a=9
b=15
# Addition: a+b
print("{0} + {1} is :".format(a,b),a+b)

# Subtraction: b-a
print("{0} - {1} is :".format(b,a),b-a)

# Multiplication: a*b
print("{0} * {1} is :".format(a,b),a*b)

# Division: b/a
print("{0} / {1} is :".format(b,a),b/a)

# Floor Division: b/a
print("{0} // {1} is :".format(b,a),b//a)

# Mod or Reminder operator: b % a
print("{0} % {1} is :".format(b,a),b%a)

# Power , b to the power a: b ** a
print("{0} ** {1} is :".format(b,a),b**a)



